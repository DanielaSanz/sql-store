IF object_id('CREATE_DB') is not null
  DROP PROC CREATE_DB; 
GO

CREATE PROC CREATE_DB
AS
BEGIN
	CREATE TABLE TYPES_CLIENT
	(
	  ID INT NOT NULL IDENTITY (1,1) PRIMARY KEY,
	  DESCRIPTION VARCHAR(30),
	  IVA FLOAT
	)

	CREATE TABLE CUSTOMERS 
	(
	  ID INT NOT NULL IDENTITY (1,1) PRIMARY KEY,
	  NAME VARCHAR(15),
	  SURNAME VARCHAR(15),
	  ID_TYPE_CLIENT INT
	)

	CREATE TABLE PRODUCTS
	(
	 ID INT NOT NULL IDENTITY (1,1) PRIMARY KEY,
	 DESCRIPTION VARCHAR(15) UNIQUE,
	 PRICE MONEY,
	 STOCK INT
	)

	CREATE TABLE BILLS
	(
	 ID INT NOT NULL IDENTITY (1,1) PRIMARY KEY,
	 ID_CUSTOMER INT
	)

	CREATE TABLE BILL_DETAILS
	(
	 ID INT NOT NULL IDENTITY (1,1) PRIMARY KEY,
	 ID_BILL INT,
	 ID_PRODUCT INT,
	 AMOUNT INT,
	 PRICE MONEY
	)
END 
GO